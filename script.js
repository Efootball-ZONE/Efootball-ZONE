let currentSlide = 0;
let sliderInterval = null;
let touchStartX = 0;
let touchEndX = 0;

function applyTranslations(lang) {
  document.querySelectorAll("[data-key]").forEach((element) => {
    const key = element.getAttribute("data-key");
    if (translations[lang] && translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });

  document.querySelectorAll("[data-placeholder-key]").forEach((element) => {
    const key = element.getAttribute("data-placeholder-key");
    if (translations[lang] && translations[lang][key]) {
      element.placeholder = translations[lang][key];
    }
  });

  if (lang === "ar") {
    document.documentElement.lang = "ar";
    document.documentElement.dir = "rtl";
  } else {
    document.documentElement.lang = lang;
    document.documentElement.dir = "ltr";
  }

  highlightActiveLanguage(lang);
}

function highlightActiveLanguage(lang) {
  const buttons = document.querySelectorAll(".lang-switcher button");
  buttons.forEach((button) => {
    const buttonLang = button.textContent.trim().toLowerCase();
    button.classList.toggle("active-lang", buttonLang === lang);
  });
}

function setLanguage(lang) {
  localStorage.setItem("siteLanguage", lang);
  applyTranslations(lang);
}

function getCurrentLanguage() {
  return localStorage.getItem("siteLanguage") || "en";
}

function highlightActiveNav() {
  const page = document.body.getAttribute("data-page");
  const navLinks = document.querySelectorAll(".navbar a");

  navLinks.forEach((link) => {
    const href = link.getAttribute("href");
    if (
      (page === "home" && href === "index.html") ||
      (page === "news" && href === "news.html") ||
      (page === "leaks" && href === "leaks.html") ||
      (page === "players" && href === "players.html") ||
      (page === "builds" && href === "builds.html") ||
      (page === "contact" && href === "contact.html")
    ) {
      link.classList.add("active-nav");
    }
  });
}

function toggleMenu() {
  const nav = document.getElementById("mobileNav");
  if (!nav) return;
  nav.classList.toggle("open");
}

function closeMenuOnDesktop() {
  const nav = document.getElementById("mobileNav");
  if (!nav) return;
  if (window.innerWidth > 768) {
    nav.classList.remove("open");
  }
}

function goToSlide(index) {
  const slides = document.querySelectorAll(".slide");
  const dots = document.querySelectorAll(".dot");
  if (!slides.length) return;

  currentSlide = index;
  if (currentSlide < 0) currentSlide = slides.length - 1;
  if (currentSlide >= slides.length) currentSlide = 0;

  slides.forEach((slide, i) => {
    slide.classList.toggle("active", i === currentSlide);
  });

  dots.forEach((dot, i) => {
    dot.classList.toggle("active", i === currentSlide);
  });
}

function changeSlide(step) {
  goToSlide(currentSlide + step);
  restartSlider();
}

function startSlider() {
  const slides = document.querySelectorAll(".slide");
  if (slides.length < 2) return;

  clearInterval(sliderInterval);
  sliderInterval = setInterval(() => {
    goToSlide(currentSlide + 1);
  }, 5000);
}

function restartSlider() {
  startSlider();
}

function setupTouchSlider() {
  const slider = document.getElementById("newsSlider");
  if (!slider) return;

  slider.addEventListener("touchstart", (e) => {
    touchStartX = e.changedTouches[0].screenX;
  });

  slider.addEventListener("touchend", (e) => {
    touchEndX = e.changedTouches[0].screenX;
    handleSwipe();
  });
}

function handleSwipe() {
  const diff = touchStartX - touchEndX;
  if (Math.abs(diff) < 40) return;

  if (diff > 0) {
    changeSlide(1);
  } else {
    changeSlide(-1);
  }
}

function setupImageZoom() {
  const zoomableImages = document.querySelectorAll(".zoomable-image");
  const modal = document.getElementById("imageModal");
  const modalImage = document.getElementById("modalImage");

  if (!zoomableImages.length || !modal || !modalImage) return;

  zoomableImages.forEach((img) => {
    img.addEventListener("click", () => {
      modal.classList.add("show");
      modalImage.src = img.src;
      modalImage.alt = img.alt;
      document.body.classList.add("no-scroll");
    });
  });

  modal.addEventListener("click", (e) => {
    if (e.target === modal) {
      closeImageModal();
    }
  });
}

function closeImageModal() {
  const modal = document.getElementById("imageModal");
  if (!modal) return;
  modal.classList.remove("show");
  document.body.classList.remove("no-scroll");
}

window.addEventListener("keydown", (e) => {
  if (e.key === "Escape") {
    closeImageModal();
  }
});

window.addEventListener("DOMContentLoaded", () => {
  const savedLanguage = getCurrentLanguage();
  applyTranslations(savedLanguage);
  highlightActiveNav();
  goToSlide(0);
  startSlider();
  setupTouchSlider();
  setupImageZoom();
  closeMenuOnDesktop();
});

window.addEventListener("resize", closeMenuOnDesktop);
