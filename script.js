function applyTranslations(lang) {
  document.querySelectorAll("[data-key]").forEach((element) => {
    const key = element.getAttribute("data-key");
    if (translations[lang] && translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });

  document.querySelectorAll("[data-placeholder-key]").forEach((element) => {
    const key = element.getAttribute("data-placeholder-key");
    if (translations[lang] && translations[lang][key]) {
      element.placeholder = translations[lang][key];
    }
  });

  if (lang === "ar") {
    document.documentElement.lang = "ar";
    document.documentElement.dir = "rtl";
  } else {
    document.documentElement.lang = lang;
    document.documentElement.dir = "ltr";
  }

  highlightActiveLanguage(lang);
}

function highlightActiveLanguage(lang) {
  const buttons = document.querySelectorAll(".lang-switcher button");
  buttons.forEach((button) => {
    const buttonLang = button.textContent.trim().toLowerCase();
    button.classList.toggle("active-lang", buttonLang === lang);
  });
}

function setLanguage(lang) {
  localStorage.setItem("siteLanguage", lang);
  applyTranslations(lang);
}

function highlightActiveNav() {
  const page = document.body.getAttribute("data-page");
  const navLinks = document.querySelectorAll(".navbar a");

  navLinks.forEach((link) => {
    const href = link.getAttribute("href");

    if (
      (page === "home" && href === "index.html") ||
      (page === "news" && href === "news.html") ||
      (page === "leaks" && href === "leaks.html") ||
      (page === "players" && href === "players.html") ||
      (page === "builds" && href === "builds.html") ||
      (page === "contact" && href === "contact.html")
    ) {
      link.classList.add("active-nav");
    }
  });
}

function filterPlayers() {
  const input = document.getElementById("playerSearch");
  if (!input) return;

  const filter = input.value.toLowerCase();
  const cards = document.querySelectorAll(".player-card");

  cards.forEach((card) => {
    const playerName = card.getAttribute("data-player");
    card.style.display = playerName.includes(filter) ? "block" : "none";
  });
}

function showContactMessage() {
  const success = document.getElementById("contactSuccess");
  if (success) {
    success.textContent = translations[getCurrentLanguage()].contact_success;
  }
}

function getCurrentLanguage() {
  return localStorage.getItem("siteLanguage") || "en";
}

window.addEventListener("DOMContentLoaded", () => {
  const savedLanguage = getCurrentLanguage();
  applyTranslations(savedLanguage);
  highlightActiveNav();
});
